function expectInvalidRouteDslFailure(name, appRoutesLines, expectedError) {
  rmSync(invalidSourceDir, { force: true, recursive: true });
  rmSync(invalidOutputDir, { force: true, recursive: true });
  mkdirSync(join(invalidSourceDir, "routes"), { recursive: true });
  writeCommonTypes(invalidSourceDir);
  writeFileSync(join(invalidSourceDir, "InvalidRouteDslMain.hx"), [
    "import routes.AppRoutes;",
    "",
    "class InvalidRouteDslMain {",
    "\tstatic function main() {",
    "\t\tvar routes:Class<AppRoutes> = AppRoutes;",
    "\t\tSys.println(routes != null);",
    "\t}",
    "}",
    "",
  ].join("\n"));
  writeFileSync(join(invalidSourceDir, "routes", "AppRoutes.hx"), appRoutesLines.join("\n"));

  const result = compileWithFirstAvailableReflaxe(invalidSourceDir, invalidOutputDir, "InvalidRouteDslMain", true);
  if (result.status === 0) {
    console.error(`[routes-dsl] invalid route DSL case compiled successfully: ${name}`);
    process.exit(1);
  }
  const output = `${result.stdout}\n${result.stderr}`;
  if (!output.includes(expectedError)) {
    console.error(`[routes-dsl] invalid route DSL case "${name}" did not fail with expected diagnostic: ${expectedError}`);
    process.stdout.write(result.stdout);
    process.stderr.write(result.stderr);
    process.exit(1);
  }
}

function compileWithFirstAvailableReflaxe(source, output, main, allowFailure = false, extraDefines = []) {
  let sawCandidate = false;
  let lastResult = null;
  for (const reflaxeSrc of reflaxeCandidates) {
    if (!existsSync(join(reflaxeSrc, "reflaxe", "ReflectCompiler.hx"))) {
      continue;
    }
    sawCandidate = true;
    const args = [
      "-D",
      `ruby_output=${output}`,
      "-D",
      "reflaxe_runtime",
      "-D",
      "reflaxe_ruby_rails",
      ...extraDefines.flatMap((define) => ["-D", define]),
      "-cp",
      join(root, "src"),
      "-cp",
      source,
      "-cp",
      reflaxeSrc,
      "--macro",
      "reflaxe.ruby.CompilerBootstrap.Start()",
      "--macro",
      "reflaxe.ruby.CompilerInit.Start()",
      "-main",
      main,
    ];
    const result = run("haxe", args, { allowFailure });
    lastResult = result;
    if (allowFailure || result.status === 0) {
      return result;
    }
  }
  if (!sawCandidate) {
    console.error("[routes-dsl] Unable to run route DSL smoke; no Reflaxe candidate found.");
    process.exit(1);
  }
  return lastResult;
}

function run(command, args, options = {}) {
  const result = spawnSync(command, args, {
    cwd: root,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
  });
  if (result.status !== 0 && !options.allowFailure) {
    process.stdout.write(result.stdout);
    process.stderr.write(result.stderr);
    process.exit(result.status ?? 1);
  }
  return result;
}
